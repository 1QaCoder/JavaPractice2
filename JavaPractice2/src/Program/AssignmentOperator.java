package Program;

public class AssignmentOperator {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int c= 30;
		int d=40;
		int e=50;
		
		a+=25;
		System.out.println(a);
		
		b-=10;
		System.out.println(b);
		
		c*=10;
		System.out.println(c);
		
		d/=4;
		System.out.println(d);
		
		e%=10;
		System.out.println(e);
		
		a=a+1;
		System.out.println(a);

	}

}
