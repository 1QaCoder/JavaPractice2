package Program;

public class ArithmaticOperators {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		
		System.out.println(a+b +"  addition -  + operator");
		System.out.println(a-b +"  minus -  - operator");
		System.out.println(a*b +"  multiplication -  * operator");
		System.out.println(b/a +"  division -  / operator");
		System.out.println(b%a +"  modulus -  % operator");
		
		
	}

}
